package pavan_sdet;

public class Week_numbers {

	public static void main(String[] args) {
		String weekname="monday";
		
		switch(weekname) {
		case "sunday":
			System.out.println(1);
			break;
		case "monday":
			System.out.println(2);
			break;
		case "tusday":
			System.out.println(3);
			break;
		case "wednessday":
			System.out.println(4);
			break;
			default:
				System.out.println(8);
		}

	}

}
